    function overMouse(){
        document.getElementById('mouseOver').onmouseover = function(){
            mouseOver();
        }
        document.getElementById('mouseOver').onmouseout = function(){
            mouseOut();
        }
    
        function mouseOver(){
            document.getElementById('mouseOver').style.backgroundColor = '#04c6c9';
        }
        function mouseOut(){
            document.getElementById('mouseOver').style.backgroundColor = '#02e5e9';
        }
    }
    overMouse();

